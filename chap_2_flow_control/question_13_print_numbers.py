# Question 13:
# Write a short program that prints the numbers 1 to 10 using a for loop.
# Then write an equivalent program that prints the numbers 1 to 10 using a while loop.

# For Loop Example
print("Using for loop:")
for i in range(1, 11):
    print(i)

# While Loop Example
print("\nUsing while loop:")
i = 1
while i <= 10:
    print(i)
    i += 1
